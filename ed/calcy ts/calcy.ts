function addition() {
    document.getElementById("submit").value = ((+document.getElementById("number1").value)
                                            + (+document.getElementById("number2").value));
}
function subtract() {
    document.getElementById("submit").value = ((+document.getElementById("number1").value)
                                            - (+document.getElementById("number2").value));
}
function multiply() {
    document.getElementById("submit").value = ((+document.getElementById("number1").value)
                                            * (+document.getElementById("number2").value));
}
function devide() {
    document.getElementById("submit").value = ((+document.getElementById("number1").value)
                                            / (+document.getElementById("number2").value));
}
